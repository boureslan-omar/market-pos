$po->execute([$id]);
$po = $po->fetch();
if (!$po) { header('Location: purchase_orders.php'); exit; }

$items = $pdo->prepare("SELECT poi.*, p.barcode FROM purchase_order_items poi LEFT JOIN products p ON p.id=poi.product_id WHERE poi.po_id=? ORDER BY poi.id");

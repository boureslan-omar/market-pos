    $lines   = [];
    $lines[] = "📋 *PURCHASE ORDER* — " . $po['po_number'];
    $lines[] = "From: *$storeName*";
    $lines[] = "To: *{$po['supplier_name']}*";
    if ($po['delivery_date']) $lines[] = "Delivery: " . date('d/m/Y', strtotime($po['delivery_date']));
    $lines[] = "";
    $lines[] = "*Items:*";
    $total = 0;
    foreach ($items as $idx => $item) {
        $n   = $idx + 1;
        $sub = $item['quantity'] * $item['estimated_price'];
        $total += $sub;
        $line = "$n. {$item['product_name']} — {$item['quantity']} {$item['unit']}";
        if ($item['estimated_price'] > 0) $line .= " @ \$" . number_format($item['estimated_price'],2) . " = \$" . number_format($sub,2);
        if ($item['note']) $line .= " ({$item['note']})";
        $lines[] = $line;
    }
    if ($total > 0) { $lines[] = ""; $lines[] = "💰 *Est. Total: \$" . number_format($total, 2) . "*"; }
    if ($po['note']) { $lines[] = ""; $lines[] = "📝 Note: " . $po['note']; }
    $lines[] = "";
    $lines[] = "_Generated by $storeName POS — " . date('d/m/Y H:i') . "_";

    $text = implode("\n", $lines);
    $url  = "https://wa.me/$phone?text=" . rawurlencode($text);
    header("Location: $url");
    exit;
}

